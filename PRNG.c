//-----------------------------------------------------------------------------
// Function   : PRNG_Init
// Purpose    : Seeds the PRNG and sets the max and min values
// Parameters : [I] iSeed: The seed of the PRNG
// Return     : None
// Notes      : None
//-----------------------------------------------------------------------------
void PRNG_Init(uint32_t iSeed)
{
	srand(iSeed);
}

//-----------------------------------------------------------------------------
// Function   : PRNG_GetNext
// Purpose    : Gets the next pseudo random value from the PRNG
// Parameters : [I] iMin: The minimum value to be generated by the PRNG
//            : [I] iMax: The maximum value to be generated by the PRNG
// Return     : The next value in the PRNG sequence
// Notes      : None
//-----------------------------------------------------------------------------
uint32_t PRNG_GetNext(uint32_t iMin, uint32_t iMax)
{
	uint32_t iRange = iMax - iMin;
	uint32_t iValue = (rand() % iRange) + iMin;

	return iValue;
}

